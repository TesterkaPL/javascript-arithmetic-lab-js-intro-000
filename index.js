function inc(n){
  return ++n
}

function dec(n){
  return --n
}

function makeInt(n){
  var parsed = parseInt(n);
  if (isNaN(parsed)) {
    return ('n', 10)
  }
  return ('n');
}

function preserveDecimal(n){
  return parseFloat('n')
}
